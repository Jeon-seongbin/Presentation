public class Team {
    private String team;
    private int winNumber;

    public String getTeam() {
        return this.team;
    }

    public void setTeam(String team) {
        this.team = team;
    }

    public int getWinNumber() {
        return this.winNumber;
    }

    public void setWinNumber(int winNumber) {
        this.winNumber = winNumber;
    }
}